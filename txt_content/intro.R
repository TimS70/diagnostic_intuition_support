    wie wahrscheinlich der Krankheitszustand war, bevor der Test durchgeführt
    wurde. Das Problem: dies auf der individuellen Ebene anhand bestehender,
    publizierter Evidenz zu bestimmen ist oft unmöglich. Ihre Intuition ist
    gefragt! Überlegen Sie sich: von 100 Menschen mit dem Risikoprofil der
    Person, an der Sie den Test durchführen bzw. durchführen möchten, wieviele
    haben COVID-19? Hat die Person sich in den letzten zwei Wochen sozial total
    isoliert und ist auch sonst keine Risiken eingegangen? Hatte sie Kontakt
    mit einer bestätigt COVID-19-positiven Person und zeigt Symptome? Es gibt zu
    dieser Frage keine absolut richtige oder falsche Antwort, sie müssen es
    einfach schätzen."


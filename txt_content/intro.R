    Test aus. Alle Tests haben eine Sensitivität und Spezifität (aktuelle
    Herstellerangaben), die angibt, wie
    genau der Test ist. Die Sensitivität ist die Wahrscheinlichkeit, dass eine
    Person, die COVID-19 hat, von dem Test korrekt als solche erkannt wird und
    einen positives Testergebnis erhält. Die Spezifizität ist die
    Wahrscheinlichkeit, dass eine gesunde Person vom Test auch korrekt als solche
    erkannt wird und ein negatives Testergebnis erhält."

